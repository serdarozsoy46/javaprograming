package day03_EscapeSequences;
/*
\n: starts anew line
\t: paragraph space tab
\\:single back slash
\":double quote

 */

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython C#");
        System.out.println("---------------");
        System.out.println("Heloo Cydeo \nHow are you all today? \nIt's nice to see you all!");
        System.out.println("\tJava is cool programming language");
        System.out.println("/ \\");
        System.out.println("my favorite tv show is game of \"throne");
        System.out.println("my favorite tv show is \"game of throne\"");
    }





}
